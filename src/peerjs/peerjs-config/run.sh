#!/bin/bash

echo "start peerjs -------------------------------"
if [ "${1}" == 'peerjs' ] ; then
  cd /peerjs
  echo "--------------------------------------------"
  echo "--- Welcom to peerjs                     ---"
  echo "--------------------------------------------"
  node app
elif [ "${1}" == "" ] ; then
  echo "--------------------------------------------"
  echo "--- Welcom to peerjs                     ---"
  echo "--------------------------------------------"
  tail -f /dev/null
else
  echo "--------------------------------------------"
  echo "--- Welcom to peerjs                     ---"
  echo "--------------------------------------------"
  exec "$@"
fi
echo "--------------------------------------------"
echo "--- exit to peerjs                       ---"
echo "--------------------------------------------"
